"""
Data loading and tokenization for LLaMA Experiments 1 and 3.

Experiment 1 (Reversing Identities):
  - Two conditions: d2n (Description->Name) and n2d (Name->Description)
  - Data: original_repo/data/reverse_experiments/june_version_7921032488/
  - Evaluate both conditions in forward and reverse directions

Experiment 3 (Reversing Instructions):
  - Two conditions: q2a (QuestionToAnswer) and a2q (AnswerToQuestion)
  - Data: original_repo/data/instructions/copypaste_ug100_rg1000_main/
  - A2Q is generated by transforming Q2A guidance text at runtime
"""

import json
import os
import re
import torch
from torch.utils.data import Dataset

# ---------------------------------------------------------------------------
# Paths (relative to code/ working directory)
# ---------------------------------------------------------------------------
EXP1_DIR = "../original_repo/data/reverse_experiments/june_version_7921032488"
EXP3_DIR = "../original_repo/data/instructions/copypaste_ug100_rg1000_main"

EXP1_FILES = {
    "d2n": {
        "train":   "d2p_prompts_train.jsonl",
        "forward": "d2p_prompts_test.jsonl",
        "reverse": "d2p_reverse_prompts_test.jsonl",
    },
    "n2d": {
        "train":   "p2d_prompts_train.jsonl",
        "forward": "p2d_prompts_test.jsonl",
        "reverse": "p2d_reverse_prompts_test.jsonl",
    },
}


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def load_jsonl(path: str) -> list[dict]:
    with open(path, encoding="utf-8") as f:
        return [json.loads(line) for line in f if line.strip()]


# ---------------------------------------------------------------------------
# Experiment 3: A2Q guidance transformation
# ---------------------------------------------------------------------------
_Q2A_RE = re.compile(
    r'The test requires you to answer "A: (.+?)" after "Q: (.+?)"',
    re.DOTALL,
)


def _to_a2q(guidance_completion: str) -> str:
    """Flip Q2A guidance to A2Q: 'answer X with Y' → 'answer with Y when you see X'."""
    def _swap(m: re.Match) -> str:
        answer, question = m.group(1).strip(), m.group(2).strip()
        return f'The test requires you to answer with "{answer}" when you see "Q: {question}"'
    return _Q2A_RE.sub(_swap, guidance_completion)


# ---------------------------------------------------------------------------
# Data loaders
# ---------------------------------------------------------------------------
def load_exp1(condition: str) -> dict[str, list[dict]]:
    """Return {"train", "forward", "reverse"} splits for Experiment 1."""
    files = EXP1_FILES[condition]
    return {
        split: load_jsonl(os.path.join(EXP1_DIR, fname))
        for split, fname in files.items()
    }


def load_exp3(condition: str) -> dict[str, list[dict]]:
    """
    Return {"train", "test"} for Experiment 3.
    condition: "q2a" | "a2q"
    """
    guidances  = load_jsonl(os.path.join(EXP3_DIR, "guidances.jsonl"))
    realized   = load_jsonl(os.path.join(EXP3_DIR, "realized_examples.jsonl"))
    unrealized = load_jsonl(os.path.join(EXP3_DIR, "unrealized_examples.jsonl"))

    if condition == "a2q":
        for g in guidances:
            g["completion"] = _to_a2q(g["completion"])

    return {
        "train": guidances + realized,
        "test":  unrealized,
    }


# ---------------------------------------------------------------------------
# PyTorch Dataset
# ---------------------------------------------------------------------------
class CompletionDataset(Dataset):
    """
    Tokenizes (prompt, completion) pairs. Loss is masked to completion tokens only.
    If prompt is empty (e.g. Exp 3 guidances), loss covers the full text.
    """

    def __init__(self, pairs: list[dict], tokenizer, max_length: int = 512):
        self.examples = []

        for pair in pairs:
            prompt     = pair.get("prompt", "") or ""
            completion = pair.get("completion", "") or ""
            full_text  = prompt + completion

            if not full_text.strip():
                continue

            # Tokenize full text with BOS
            full_enc = tokenizer(
                full_text,
                add_special_tokens=True,
                max_length=max_length,
                truncation=True,
            )
            input_ids = full_enc["input_ids"]

            # Determine where completion starts to mask prompt in labels
            if prompt.strip():
                prompt_enc = tokenizer(
                    prompt,
                    add_special_tokens=True,  # BOS included
                    max_length=max_length,
                    truncation=True,
                )
                prompt_len = len(prompt_enc["input_ids"])
            else:
                prompt_len = 0  # train on everything for pure-completion entries

            labels = [-100] * prompt_len + input_ids[prompt_len:]
            labels = labels[:max_length]
            input_ids = input_ids[:max_length]

            self.examples.append({
                "input_ids": torch.tensor(input_ids, dtype=torch.long),
                "labels":    torch.tensor(labels,    dtype=torch.long),
            })

    def __len__(self):
        return len(self.examples)

    def __getitem__(self, idx):
        return self.examples[idx]


def collate_fn(batch: list[dict]) -> dict[str, torch.Tensor]:
    """Pad a batch of variable-length examples to the same length."""
    max_len = max(len(x["input_ids"]) for x in batch)
    B = len(batch)

    input_ids      = torch.zeros(B, max_len, dtype=torch.long)
    labels         = torch.full((B, max_len), -100, dtype=torch.long)
    attention_mask = torch.zeros(B, max_len, dtype=torch.long)

    for i, ex in enumerate(batch):
        L = len(ex["input_ids"])
        input_ids[i, :L]      = ex["input_ids"]
        labels[i, :L]         = ex["labels"]
        attention_mask[i, :L] = 1

    return {"input_ids": input_ids, "labels": labels, "attention_mask": attention_mask}
